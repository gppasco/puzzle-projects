# TRANSLATING THAT BAD BOY
from multiprocessing.dummy import Pool as ThreadPool
import time

pool = ThreadPool(8) # Threads

def request(text):
    lang = "fr"
    t = google_translator(timeout=5)
    # TRANSLATES TO RANDOM LANGUAGES A RANDOM NUMBER OF TIMES
    num_translations = 5
    translate1 = t.translate(text.strip(), lang)
    translate2 = t.translate(translate1, "en")
    translate3 = t.translate(translate2, lang)
    translateFINAL = t.translate(translate3, "en")
    translateFINAL = re.sub("\u201c", "\"", translateFINAL)

    return translateFINAL

if __name__ == "__main__" :
    time1 = time.time()
    texts = p.clues
    print("Translating...")
    results = pool.map(request, texts)
    pool.close()
    pool.join()

    time2 = time.time()
    print("Translating %s sentences, a total of %s s"%(len(texts),time2 - time1))



p.clues = results

p.save('translated.puz')